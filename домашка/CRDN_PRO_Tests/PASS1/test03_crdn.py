# Створіть функцію num_split яка на вхід отримує один аргумент, що є цілим невід'ємним числом та розбиває його
# на цифри. Повертає список цифр з яких складається число.


def num_split(n):
    if n == 0:
        return [0]

    res = []
    while n > 0:
        res.insert(0, n % 10)
        n = n // 10
    return res


print(num_split(24))